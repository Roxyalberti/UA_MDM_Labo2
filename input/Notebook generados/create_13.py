TRAIN_CODE = {
    "cell_type": "code",
    "execution_count": None,
    "id": "train_code",
    "metadata": {},
    "outputs": [],
    "source": [
        "skf = StratifiedKFold(n_splits=5, shuffle=True, random_state=SEED)\n",
        "\n",
        "lgb_oof  = np.zeros((len(X_train), 5)); xgb_oof  = np.zeros((len(X_train), 5)); cb_oof  = np.zeros((len(X_train), 5))\n",
        "lgb_test = np.zeros((len(X_test),  5)); xgb_test = np.zeros((len(X_test),  5)); cb_test = np.zeros((len(X_test),  5))\n",
        "\n",
        "lgb_params = {\n",
        "    'objective': 'multiclass', 'num_class': 5, 'verbosity': -1,\n",
        "    'num_leaves': 51, 'lambda_l1': 0.10, 'lambda_l2': 7.58,\n",
        "    'feature_fraction': 0.59, 'bagging_fraction': 0.98,\n",
        "    'bagging_freq': 1, 'min_child_samples': 118, 'learning_rate': 0.093\n",
        "}\n",
        "xgb_params = {\n",
        "    'objective': 'multi:softprob', 'num_class': 5, 'verbosity': 0,\n",
        "    'max_depth': 6, 'learning_rate': 0.05, 'n_estimators': 500,\n",
        "    'subsample': 0.8, 'colsample_bytree': 0.8, 'reg_alpha': 0.1,\n",
        "    'reg_lambda': 1.0, 'min_child_weight': 5, 'random_state': SEED,\n",
        "    'tree_method': 'hist', 'device': 'cuda'\n",
        "}\n",
        "cb_params = {\n",
        "    'loss_function': 'MultiClass', 'eval_metric': 'Accuracy',\n",
        "    'random_seed': SEED, 'verbose': False, 'early_stopping_rounds': 20,\n",
        "    'iterations': 800, 'depth': 6, 'learning_rate': 0.19071087606036805,\n",
        "    'l2_leaf_reg': 19.723764128240514, 'bagging_temperature': 0.22376847772452582,\n",
        "    'border_count': 196, 'min_data_in_leaf': 13, 'task_type': 'GPU'\n",
        "}\n",
        "\n",
        "for fold, (tr_idx, val_idx) in enumerate(skf.split(X_train, y_train)):\n",
        "    print(f'  Fold {fold+1}/5...', end=' ')\n",
        "    X_tr, X_val = X_train.iloc[tr_idx], X_train.iloc[val_idx]\n",
        "    y_tr, y_val = y_train.iloc[tr_idx], y_train.iloc[val_idx]\n",
        "\n",
        "    m_lgb = lgb.train(lgb_params, lgb.Dataset(X_tr, label=y_tr), num_boost_round=500,\n",
        "                      valid_sets=[lgb.Dataset(X_val, label=y_val)],\n",
        "                      callbacks=[lgb.early_stopping(20, verbose=False)])\n",
        "    lgb_oof[val_idx] = m_lgb.predict(X_val)\n",
        "    lgb_test += m_lgb.predict(X_test)\n",
        "\n",
        "    m_xgb = xgb.XGBClassifier(**xgb_params)\n",
        "    m_xgb.fit(X_tr, y_tr, eval_set=[(X_val, y_val)], verbose=False)\n",
        "    xgb_oof[val_idx] = m_xgb.predict_proba(X_val)\n",
        "    xgb_test += m_xgb.predict_proba(X_test)\n",
        "\n",
        "    m_cb = CatBoostClassifier(**cb_params)\n",
        "    m_cb.fit(X_tr, y_tr, eval_set=(X_val, y_val), use_best_model=True)\n",
        "    cb_oof[val_idx] = m_cb.predict_proba(X_val)\n",
        "    cb_test += m_cb.predict_proba(X_test)\n",
        "    print('OK')\n",
        "\n",
        "def norm(arr): return arr / arr.sum(axis=1, keepdims=True)\n",
        "lgb_oof_n  = norm(lgb_oof);  xgb_oof_n  = norm(xgb_oof);  cb_oof_n  = norm(cb_oof)\n",
        "lgb_test_n = norm(lgb_test); xgb_test_n = norm(xgb_test); cb_test_n = norm(cb_test)\n",
        "\n",
        "W_LGB, W_XGB, W_CB = 0.30, 0.45, 0.25\n",
        "oof_blend  = W_LGB * lgb_oof_n  + W_XGB * xgb_oof_n  + W_CB * cb_oof_n\n",
        "test_blend = W_LGB * lgb_test_n + W_XGB * xgb_test_n + W_CB * cb_test_n\n",
        "\n",
        "CLASS_WEIGHTS = np.array([0, 1, 2, 3, 4])\n",
        "oof_score  = oof_blend  @ CLASS_WEIGHTS\n",
        "test_score = test_blend @ CLASS_WEIGHTS\n",
        "\n",
        "def apply_thresholds(score, thresholds):\n",
        "    return np.clip(np.digitize(score, bins=np.sort(thresholds)), 0, 4)\n",
        "\n",
        "def kappa_loss(thresholds, score, y):\n",
        "    return -cohen_kappa_score(y, apply_thresholds(score, thresholds), weights='quadratic')\n",
        "\n",
        "print('Optimizando umbrales...')\n",
        "result = minimize(partial(kappa_loss, score=oof_score, y=y_train.values),\n",
        "                  [0.5, 1.5, 2.5, 3.5], method='nelder-mead',\n",
        "                  options={'xatol': 1e-6, 'fatol': 1e-6, 'maxiter': 20000})\n",
        "optimal_thresholds = np.sort(result.x)\n",
        "\n",
        "test_preds_opt = apply_thresholds(test_score, optimal_thresholds)\n",
        "kappa_bert = cohen_kappa_score(y_test, test_preds_opt, weights='quadratic')\n",
        "print(f'Umbrales optimos: {[f\"{t:.4f}\" for t in optimal_thresholds]}')\n",
        "print(f'\\nEnsemble + BERT PCA32 + Opt. Rounder — Test kappa: {kappa_bert:.4f}')\n"
    ]
}

