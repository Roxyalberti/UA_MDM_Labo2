SEC_C = {"cell_type": "markdown", "id": "sec_c_md", "metadata": {}, "source": [
    "## Seccion C: PCA — reduccion de dimensionalidad\n\n",
    "Reducimos los 384 dims de BERT a **32 componentes principales**.\n\n",
    "**¿Por que PCA?**\n",
    "- 384 features de BERT + 48 de FE v4 = 432 features totales: demasiado para los arboles\n",
    "- PCA captura las 32 direcciones de mayor varianza en el espacio semantico\n",
    "- **Importante:** fit del PCA solo sobre el train set, luego transform en test (sin data leakage)\n\n",
    "Con 32 componentes retenemos tipicamente ~70-80% de la varianza total de BERT."
]}

